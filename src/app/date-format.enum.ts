export enum DateFormat {
    // default date display format
    LONG_DATE = 'd MMM y',
    // default date time display format
    LONG_DATE_TIME = 'd MMM y, HH:mm',
    // default time display format
    TIME = 'HH:mm',
    // backend date format (ISO-8601)
    ISO_DATE = 'yyyy-MM-dd',
    // only day and month
    MONTH_DAY = 'd MMM',
    // only month and year
    MONTH_YEAR = 'MMMM y',
    // only day of week
    WEEK_DAY = 'EEEE'
}
